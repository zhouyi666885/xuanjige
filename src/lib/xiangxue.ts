/**
 * 从面相照片生成面相预测框架
 * AI 会根据照片和此框架进行具体分析
 */
export function generateMianXiangFramework(
  birthYear: number,
  currentYear: number,
  gender: string
): string {
  const age = currentYear - birthYear;
  const liuNian = LIU_NIAN_BU_WEI[age] || { buwei: '综合流年', weizhi: '全面观察' };

  let output = `=== 面相分析框架（代码计算，非AI脑补） ===\n`;
  output += `当前年龄：${age}岁 | 性别：${gender === '男' ? '男命（左起右行）' : '女命（右起左行）'}\n`;
  output += `流年部位：${liuNian.buwei}（${liuNian.weizhi}）—— 此部位气色好坏决定${age}岁当年运势\n\n`;

  output += `=== 五官论断（《麻衣神相》《冰鉴》） ===\n`;
  for (const [organ, luduan] of Object.entries(WU_GUAN_LUN_DUAN)) {
    output += `【${organ}】\n  吉：${luduan.good}\n  凶：${luduan.bad}\n  经典：${luduan.classic}\n\n`;
  }

  output += `=== 十二宫论断 ===\n`;
  for (const [gong, luduan] of Object.entries(SHI_ER_GONG_LUN_DUAN)) {
    output += `【${gong}】（${luduan.weizhi}）\n  吉：${luduan.good}\n  凶：${luduan.bad}\n  经典：${luduan.classic}\n\n`;
  }

  output += `=== 三停论断 ===\n`;
  for (const [ting, luduan] of Object.entries(SAN_TING_LUN_DUAN)) {
    output += `【${ting}】（${luduan.fanwei}）\n  吉：${luduan.good}\n  凶：${luduan.bad}\n  经典：${luduan.classic}\n\n`;
  }

  output += `=== 气色论（《水镜神相》《铁关刀》） ===\n`;
  for (const [color, luduan] of Object.entries(QI_SE_LUN)) {
    output += `【${color}】${luduan.hanYi}\n  吉凶：${luduan.zhuJiXiong}\n  经典：${luduan.classic}\n\n`;
  }

  return output;
}

