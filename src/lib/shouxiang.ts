/**
 * 生成手相分析框架
 * AI 根据照片和此框架进行分析
 */
export function generateShouXiangFramework(
  birthYear: number,
  currentYear: number,
  gender: string
): string {
  const age = currentYear - birthYear;

  let output = `=== 手相分析框架（代码计算，非AI脑补） ===\n`;
  output += `当前年龄：${age}岁 | 性别：${gender}\n\n`;

  output += `=== 五大主线论断 ===\n`;
  for (const [line, luduan] of Object.entries(ZHU_XIAN_LUN_DUAN)) {
    output += `【${line}】（${luduan.weizhi}）\n`;
    output += `  含义：${luduan.hanYi}\n`;
    output += `  吉：${luduan.good}\n`;
    output += `  凶：${luduan.bad}\n`;
    output += `  流年法：${luduan.liuNianMethod}\n`;
    output += `  经典：${luduan.classic}\n\n`;
  }

  output += `=== 九大丘位论断 ===\n`;
  for (const [qiu, luduan] of Object.entries(QIU_WEI_LUN_DUAN)) {
    output += `【${qiu}】（${luduan.weizhi}）\n`;
    output += `  含义：${luduan.hanYi}\n`;
    output += `  吉：${luduan.good}\n`;
    output += `  凶：${luduan.bad}\n\n`;
  }

  output += `=== 特殊纹路 ===\n`;
  for (const [wen, luduan] of Object.entries(TE_SHU_WEN_LU)) {
    output += `【${wen}】（${luduan.weizhi}）\n`;
    output += `  含义：${luduan.hanYi}\n`;
    output += `  吉凶：${luduan.jixiong}\n\n`;
  }

  return output;
}

