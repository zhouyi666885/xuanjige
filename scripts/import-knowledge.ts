const chunkConfig: ChunkConfig = {
  separator: '\n\n',
  max_tokens: 1000,
  remove_extra_spaces: false,
  remove_urls_emails: false,
};

async function importAll() {
  console.log('开始导入知识库...\n');

  let totalDocs = 0;
  let totalSuccess = 0;
  let totalFail = 0;

  for (const [domain, books] of Object.entries(allDomainBooks)) {
    const dataset = DATASET_MAP[domain];
    console.log(`\n📂 导入领域：${domain} → 数据集：${dataset}`);
    console.log(`   共 ${books.length} 本书\n`);

    for (let i = 0; i < books.length; i++) {
      const book = books[i];
      const preview = book.raw_data?.substring(0, 80) || book.url?.substring(0, 80) || 'unknown';
      console.log(`   📖 [${i + 1}/${books.length}] ${preview.replace(/\n/g, ' ')}...`);

      try {
        const response = await client.addDocuments([book], dataset, chunkConfig);

        if (response.code === 0) {
          console.log(`   ✅ 成功导入 (IDs: ${response.doc_ids?.join(', ')})`);
          totalSuccess++;
        } else {
          console.log(`   ❌ 导入失败: ${response.msg}`);
          totalFail++;
        }
      } catch (error) {
        console.log(`   ❌ 导入异常: ${error}`);
        totalFail++;
      }

      totalDocs++;

      // 限制请求频率，避免过快
      if (totalDocs % 5 === 0) {
        console.log(`   ⏳ 等待1秒...`);
        await new Promise((resolve) => setTimeout(resolve, 1000));
      }
    }
  }

  console.log('\n\n========================================');
  console.log('导入完成！');
  console.log(`总文档数：${totalDocs}`);
  console.log(`成功：${totalSuccess}`);
  console.log(`失败：${totalFail}`);
  console.log('========================================');
}

importAll().catch(console.error);
