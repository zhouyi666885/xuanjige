async function importRules() {
  const allRules = [
    ...xueyeRules,
    ...hunyinRules,
    ...caiyunRules,
    ...shiyeRules,
    ...jiankangRules,
    ...liuqinRules,
  ];

  console.log(`共 ${allRules.length} 条铁律级规则待导入到 ${DATASET}`);

  let success = 0;
  let fail = 0;

  for (let i = 0; i < allRules.length; i++) {
    const rule = allRules[i];
    const preview = rule.raw_data?.substring(0, 60) || 'unknown';
    console.log(`\n[${i + 1}/${allRules.length}] ${preview.replace(/\n/g, ' ')}...`);

    try {
      const response = await client.addDocuments([rule], DATASET, chunkConfig);

      if (response.code === 0) {
        console.log(`✅ 成功导入 (IDs: ${response.doc_ids?.join(', ')})`);
        success++;
      } else {
        console.log(`❌ 导入失败: ${response.msg}`);
        fail++;
      }
    } catch (error: unknown) {
      const msg = error instanceof Error ? error.message : String(error);
      console.log(`❌ 导入异常: ${msg}`);
      fail++;
    }

    // 限制请求频率
    if ((i + 1) % 5 === 0) {
      console.log('⏳ 等待1秒...');
      await new Promise((resolve) => setTimeout(resolve, 1000));
    }
  }

  console.log(`\n═══════════════════════════════════`);
  console.log(`导入完成！成功：${success}，失败：${fail}，总计：${allRules.length}`);
}

importRules().catch(console.error);
